package main

import "fmt"

func main() {
	i := 99

	var x int
	var f float32
	var s string
	var b bool

	fmt.Printf("The value of i is : %d\n", i)
	fmt.Printf("The type of i is : %T\n", i)
	fmt.Printf("The value of x is : %d\n", x)
	fmt.Printf("The value of f is : %f\n", f)
	fmt.Printf("The value of s is : -%s-\n", s)
	fmt.Printf("The value of b is : %v\n", b)
	fmt.Printf("The type of b is : %T\n", b)

}
